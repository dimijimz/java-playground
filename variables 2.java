// here we see how we use the type "double" to work with decimals and then "int" to work with regular integers
class Demo {
    public static void main(String[] args) {
        double weight = 12.5;

        System.out.println(weight);
    }
}
//output: 12.5

//int
class Demo {
    public static void main(String[] args) {
        int age = 29;

        System.out.println(age);
    }
}
//output: 29